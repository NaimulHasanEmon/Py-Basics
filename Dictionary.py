# We will use dictionary in python when we will be needed to store key and value

marks = {"English": 97, "Math": 100}

# Printing the dictionary
print(marks)

# Printing the marks of English only
print(marks["English"])

# Adding a new mark to the dictionary
marks["Physics"] = 96
print(marks)

# Updating an existing mark to the dictionary
marks["Physics"] = 99
print(marks)
